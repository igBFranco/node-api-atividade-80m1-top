class Restaurant {
    constructor(id, address, city, description, name, state, tag, zipcode) {
        this.id = id,
        this.address = address,
        this.city = city,
        this.description = description,
        this.name = name,
        this.state = state,
        this.tag = tag,
        this.zipcode = zipcode
    }
}

module.exports = Restaurant;